// Type Annotation is used to specify explicit types for 
// identifiers such as variables, functions, objects, etc.

// string type
let username:string;
username="ambika";
console.log(`(string) user name: ${username}`);

//number type
let age:number;
age=12;
console.log(`(number) age: ${age}`);

//boolean type
let isMajor:boolean;
isMajor=false;
console.log(`(boolean) is Major: ${isMajor}`);


